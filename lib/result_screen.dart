  Widget _buildComparisons({
    required BuildContext context,
    required ThemeData theme,
    required ColorScheme colorScheme,
    required bool isDark,
    required Color textColor,
    required Color textDimColor,
    required bool isHindi,
  }) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.stretch,
      children: [
        Row(
          children: [
            Container(
              width: 3.w,
              height: 20.h,
              decoration: BoxDecoration(
                gradient: LinearGradient(
                    colors: [colorScheme.primary, colorScheme.secondary],
                    begin: Alignment.topCenter,
                    end: Alignment.bottomCenter),
                borderRadius: BorderRadius.circular(2.r),
              ),
            ),
            SizedBox(width: 10.w),
            Text(
              tr('Detailed Comparison', isHindi),
              style: GoogleFonts.inter(
                  fontSize: 16.sp, fontWeight: FontWeight.w800, color: textColor),
            ),
          ],
        ),
        SizedBox(height: 4.h),
        Text(
          'Long-press any card for options',
          style: GoogleFonts.inter(fontSize: 11.sp, color: textDimColor.withOpacity(0.6)),
        ),
        SizedBox(height: 14.h),
        ...List.generate(widget.resultData['comparisons'].length, (index) {
          final comp = widget.resultData['comparisons'][index];
          return StaggeredListItem(
            index: index,
            child: _buildComparisonCard(
              context: context,
              field: comp['field'],
              doc1Value: comp['doc1'],
              doc2Value: comp['doc2'],
              status: comp['status'],
              theme: theme,
              colorScheme: colorScheme,
              isDark: isDark,
              textColor: textColor,
              textDimColor: textDimColor,
              isHindi: isHindi,
            ),
          );
        }),
      ],
    );
  }

  Widget _buildComparisonCard({
    required BuildContext context,
    required String field,
    required String doc1Value,
    required String doc2Value,
    required String status,
    required ThemeData theme,
    required ColorScheme colorScheme,
    required bool isDark,
    required Color textColor,
    required Color textDimColor,
    required bool isHindi,
  }) {
    Color badgeColor;
    IconData statusIcon;
    if (status == 'Match') {
      badgeColor = const Color(0xFF10B981);
      statusIcon = Icons.check_circle_rounded;
    } else if (status == 'Partial') {
      badgeColor = const Color(0xFFF59E0B);
      statusIcon = Icons.warning_rounded;
    } else {
      badgeColor = const Color(0xFFEF4444);
      statusIcon = Icons.cancel_rounded;
    }

    return GestureDetector(
      onLongPress: () {
        HapticFeedback.lightImpact();
        _showCardOptions(context, field, doc1Value, doc2Value, status);
      },
      child: Container(
        margin: EdgeInsets.only(bottom: 12.h),
        decoration: BoxDecoration(
          color: theme.cardColor,
          borderRadius: BorderRadius.circular(20.r),
          border: Border.all(color: badgeColor.withOpacity(0.15), width: 1.w),
          boxShadow: [
            BoxShadow(
              color: Colors.black.withOpacity(isDark ? 0.15 : 0.04),
              blurRadius: 10.r,
              offset: Offset(0, 3.h),
            ),
          ],
        ),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.stretch,
          children: [
            // Header
            Padding(
              padding: EdgeInsets.fromLTRB(16.w, 14.h, 12.w, 10.h),
              child: Row(
                children: [
                  Container(
                    padding: EdgeInsets.all(8.w),
                    decoration: BoxDecoration(
                      color: badgeColor.withOpacity(0.1),
                      borderRadius: BorderRadius.circular(10.r),
                    ),
                    child: Icon(statusIcon, color: badgeColor, size: 16.sp),
                  ),
                  SizedBox(width: 10.w),
                  Expanded(
                    child: Text(
                      field,
                      style: GoogleFonts.inter(
                        fontSize: 14.sp,
                        fontWeight: FontWeight.w700,
                        color: textColor,
                      ),
                      overflow: TextOverflow.ellipsis,
                    ),
                  ),
                  SizedBox(width: 8.w),
                  Container(
                    padding: EdgeInsets.symmetric(horizontal: 10.w, vertical: 5.h),
                    decoration: BoxDecoration(
                      color: badgeColor.withOpacity(0.1),
                      borderRadius: BorderRadius.circular(10.r),
                      border: Border.all(color: badgeColor.withOpacity(0.3)),
                    ),
                    child: Text(
                      status.toUpperCase(),
                      style: GoogleFonts.inter(
                          fontSize: 10.sp,
                          fontWeight: FontWeight.w800,
                          color: badgeColor,
                          letterSpacing: 0.5),
                    ),
                  ),
                ],
              ),
            ),

            // Divider
            Divider(height: 1.h, color: colorScheme.onSurface.withOpacity(0.06)),

            // Value comparison
            Padding(
              padding: EdgeInsets.all(16.w),
              child: Row(
                children: [
                  // Doc 1
                  Expanded(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Container(
                          padding: EdgeInsets.symmetric(horizontal: 8.w, vertical: 3.h),
                          decoration: BoxDecoration(
                            color: const Color(0xFF6366F1).withOpacity(0.08),
                            borderRadius: BorderRadius.circular(6.r),
                          ),
                          child: Text(
                            tr('DOCUMENT 1', isHindi),
                            style: GoogleFonts.inter(
                              fontSize: 9.sp,
                              color: const Color(0xFF6366F1),
                              letterSpacing: 0.8,
                              fontWeight: FontWeight.w700,
                            ),
                          ),
                        ),
                        SizedBox(height: 8.h),
                        Text(
                          doc1Value,
                          style: GoogleFonts.inter(
                            fontSize: 13.sp,
                            color: textColor,
                            fontWeight: FontWeight.w600,
                          ),
                        ),
                      ],
                    ),
                  ),

                  // VS separator
                  Padding(
                    padding: EdgeInsets.symmetric(horizontal: 12.w),
                    child: Column(
                      children: [
                        Container(
                          width: 1.w,
                          height: 16.h,
                          color: colorScheme.onSurface.withOpacity(0.1),
                        ),
                        Padding(
                          padding: EdgeInsets.symmetric(vertical: 4.h),
                          child: Text(
                            'VS',
                            style: GoogleFonts.inter(
                              fontSize: 9.sp,
                              fontWeight: FontWeight.w700,
                              color: textDimColor.withOpacity(0.5),
                            ),
                          ),
                        ),
                        Container(
                          width: 1.w,
                          height: 16.h,
                          color: colorScheme.onSurface.withOpacity(0.1),
                        ),
                      ],
                    ),
                  ),

                  // Doc 2
                  Expanded(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Container(
                          padding: EdgeInsets.symmetric(horizontal: 8.w, vertical: 3.h),
                          decoration: BoxDecoration(
                            color: const Color(0xFF8B5CF6).withOpacity(0.08),
                            borderRadius: BorderRadius.circular(6.r),
                          ),
                          child: Text(
                            tr('DOCUMENT 2', isHindi),
                            style: GoogleFonts.inter(
                              fontSize: 9.sp,
                              color: const Color(0xFF8B5CF6),
                              letterSpacing: 0.8,
                              fontWeight: FontWeight.w700,
                            ),
                          ),
                        ),
                        SizedBox(height: 8.h),
                        Text(
                          doc2Value,
                          style: GoogleFonts.inter(
                            fontSize: 13.sp,
                            color: status == 'Mismatch'
                                ? const Color(0xFFEF4444)
                                : textColor,
                            fontWeight: FontWeight.w600,
                          ),
                        ),
                      ],
                    ),
                  ),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }

