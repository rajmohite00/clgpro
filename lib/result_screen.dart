  Widget _buildFraudMeter({
    required BuildContext context,
    required int fraudScore,
    required bool isDark,
    required Color textColor,
    required Color textDimColor,
    required bool isHindi,
  }) {
    final theme = Theme.of(context);

    Color meterColor;
    String meterLabel;
    String riskDesc;
    if (fraudScore <= 30) {
      meterColor = const Color(0xFF10B981);
      meterLabel = 'Low Risk';
      riskDesc = 'Document appears genuine';
    } else if (fraudScore <= 70) {
      meterColor = const Color(0xFFF59E0B);
      meterLabel = 'Medium Risk';
      riskDesc = 'Some discrepancies found';
    } else {
      meterColor = const Color(0xFFEF4444);
      meterLabel = 'High Risk';
      riskDesc = 'Significant fraud indicators';
    }

    return Container(
      padding: EdgeInsets.all(20.w),
      decoration: BoxDecoration(
        color: theme.cardColor,
        borderRadius: BorderRadius.circular(24.r),
        border: Border.all(color: meterColor.withOpacity(0.2)),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withOpacity(isDark ? 0.2 : 0.04),
            blurRadius: 14.r,
            offset: Offset(0, 4.h),
          ),
        ],
      ),
      child: Row(
        children: [
          // Circular meter
          AnimatedBuilder(
            animation: _meterProgress,
            builder: (context, _) {
              return SizedBox(
                width: 90.w,
                height: 90.w,
                child: Stack(
                  alignment: Alignment.center,
                  children: [
                    SizedBox(
                      width: 90.w,
                      height: 90.w,
                      child: CircularProgressIndicator(
                        value: _meterProgress.value,
                        strokeWidth: 10.w,
                        strokeCap: StrokeCap.round,
                        backgroundColor: meterColor.withOpacity(0.1),
                        valueColor: AlwaysStoppedAnimation<Color>(meterColor),
                      ),
                    ),
                    Column(
                      mainAxisSize: MainAxisSize.min,
                      children: [
                        Text(
                          '${fraudScore}%',
                          style: GoogleFonts.inter(
                            fontSize: 20.sp,
                            fontWeight: FontWeight.w900,
                            color: meterColor,
                          ),
                        ),
                      ],
                    ),
                  ],
                ),
              );
            },
          ),
          SizedBox(width: 20.w),

          // Labels
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  'Fraud Risk Score',
                  style: GoogleFonts.inter(
                    fontSize: 12.sp,
                    fontWeight: FontWeight.w600,
                    color: textDimColor,
                    letterSpacing: 0.5,
                  ),
                ),
                SizedBox(height: 6.h),
                Text(
                  meterLabel,
                  style: GoogleFonts.inter(
                    fontSize: 20.sp,
                    fontWeight: FontWeight.w900,
                    color: meterColor,
                    letterSpacing: -0.3,
                  ),
                ),
                SizedBox(height: 4.h),
                Text(
                  riskDesc,
                  style: GoogleFonts.inter(
                    fontSize: 12.sp,
                    color: textDimColor,
                  ),
                ),
                SizedBox(height: 10.h),
                // Mini score bar
                Container(
                  height: 6.h,
                  decoration: BoxDecoration(
                    color: meterColor.withOpacity(0.1),
                    borderRadius: BorderRadius.circular(3.r),
                  ),
                  child: AnimatedBuilder(
                    animation: _meterProgress,
                    builder: (context, _) {
                      return FractionallySizedBox(
                        alignment: Alignment.centerLeft,
                        widthFactor: _meterProgress.value,
                        child: Container(
                          decoration: BoxDecoration(
                            gradient: LinearGradient(
                              colors: [meterColor.withOpacity(0.7), meterColor],
                            ),
                            borderRadius: BorderRadius.circular(3.r),
                            boxShadow: [
                              BoxShadow(
                                color: meterColor.withOpacity(0.4),
                                blurRadius: 4.r,
                              ),
                            ],
                          ),
                        ),
                      );
                    },
                  ),
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }

